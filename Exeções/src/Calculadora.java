
public class Calculadora {

     int a, b;

    public Calculadora(int a, int b){
        this.a = a;
        this.b = b;
    }

    public int soma (){

        return a + b;
    }

    public int subtracao (){

        return a - b;
    }

    public int divisao (){

        return a / b;
    }

    public int multiplicacao (){

        return a * b;
    }
}
